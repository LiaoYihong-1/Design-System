uint8_t last = 'a';
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	static int phase = 0;
	if(htim->Instance == TIM6){
		phase++;
		if (phase > 4) phase = 0;
		int pulse = 0;
		switch(phase){
			case 0: pulse = 0; break;
			case 1: pulse = 10; break;
			case 2: pulse = 30; break;
			case 3: pulse = 60; break;
			case 4: pulse = 80; break;
		}
		switch(mode){
		/*
		 * 2 - green
		 * 3 - red
		 * 4 - yellow
		 */
		case 'a':
			htim4.Instance->CCR2 = pulse;
			htim4.Instance->CCR3 = pulse;
			break;
		case 'b':
			htim4.Instance->CCR2 = pulse;
			htim4.Instance->CCR4 = pulse;
			break;
		case 'c':
			htim4.Instance->CCR2 = pulse;
			htim4.Instance->CCR3 = 80-pulse;
			break;
		case 'd':
			htim4.Instance->CCR2 = pulse;
			htim4.Instance->CCR4 = 80-pulse;
			break;
		default:
			break;
		}
	}
}
